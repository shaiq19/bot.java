package com.example.webscraper.controller;

public class Customer {
    private int id;
    private String name;
    private Double cell;

    public Customer() {

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getCell() {
        return cell;
    }

    public void setCell(Double cell) {
        this.cell = cell;
    }
}
